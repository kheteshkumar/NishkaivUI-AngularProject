@Injectable({ providedIn: 'root' })
export class ThemeService {

  constructor() { }

  changeTheme(data) {
    let selectedTheme;
    switch (data) {
      case 7:
        selectedTheme = skin7;
        break;
      case 0:
        selectedTheme = skin7;
        break;
      case 1:
        selectedTheme = skin1;
        break;
      case 2:
        selectedTheme = skin2;
        break;
      case 3:
        selectedTheme = skin3;
        break;
      case 4:
        selectedTheme = skin4;
        break;
      case 5:
        selectedTheme = skin5;
        break;
      case 6:
        selectedTheme = skin6;
        break;
      default:
        break;
    }
    this.setTheme(selectedTheme);
    document.getElementById('appFavicon').setAttribute('href', `../../../../assets/images/skins/favicons/skin${data}.ico`);
  }

  private setTheme(theme: {}) {
    Object.keys(theme).forEach(k =>
      document.documentElement.style.setProperty(`--${k}`, theme[k])
    );
  }
}

